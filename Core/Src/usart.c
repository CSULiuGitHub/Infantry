void UART1_RemoteStart(void)
{
    HAL_DMA_RegisterCallback(&hdma_usart1_rx,HAL_DMA_XFER_CPLT_CB_ID,uart1_dma_callback);
    
    SET_BIT(huart1.Instance->CR3, USART_CR3_DMAR);
    HAL_DMA_Start_IT(&hdma_usart1_rx, (uint32_t)(&USART1->DR), \
    (uint32_t)&uart1_buf[0], 18);
}

void UART3_JudgeStart(void)
{
    HAL_DMA_RegisterCallback(&hdma_usart3_rx,HAL_DMA_XFER_CPLT_CB_ID,uart3_dma_m0_callback);
    HAL_DMA_RegisterCallback(&hdma_usart3_rx,HAL_DMA_XFER_M1CPLT_CB_ID,uart3_dma_m1_callback);
    
    HAL_UART_Receive_IT(&huart3, &uart_tmp, 1);
    SET_BIT(huart3.Instance->CR3, USART_CR3_DMAR);     
    HAL_DMAEx_MultiBufferStart_IT(&hdma_usart3_rx, (uint32_t)(&USART3->DR), \
    (uint32_t)&uart3_m0_buf[0], (uint32_t)&uart3_m1_buf[0], 1024);

}
